// Task 8: Countdown Timer
// Write a program that displays a countdown from 10 to 1.

// After the countdown is completed, display the following message:

// Blast Off 🚀

// Expected Output
// 10

// 9

// 8

// 7

// ...

// 1

// Blast Off 🚀

let countdown = 10;

while (countdown >= 1) {
    
  document.write(`${countdown} </br>`)

    countdown--
    
}
document.write(`Blast Off 🚀 </br>`);





