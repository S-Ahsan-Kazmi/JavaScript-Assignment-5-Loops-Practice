// Task 9: Dynamic Counting
// Take a number from the user using prompt().

// Display all numbers from 1 up to the number entered by the user.

// Example
// Input:

// 7

// Output:

// 1

// 2

// 3

// 4

// 5

// 6

// 7

let input = prompt("Enter Your Number","");

for(let i = 1; i <= input; i++){

    document.write(`${i} </br>`)
    
}







